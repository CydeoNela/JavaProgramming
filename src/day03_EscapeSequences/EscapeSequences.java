package day03_EscapeSequences;

/*
Escape sequences: MUST be given with double quote
\n: starts a new line
\t:paragraph space(tab)
\\:single back slash
\": double quote


 */
public class EscapeSequences {

    public static void main(String[] args) {
        System.out.println("Java \n Python \n C#");

        System.out.println("------------------------");

        System.out.println( "Hello Cydeo\n How are you all? \n It's nice to see you all! \n What class do we have next week?");
        System.out.println("------------------------------");

        System.out.println("\tJava is Cool Programming Language");

        System.out.println("----------------------------------------");

        System.out.println("/ \\"); //In order to print one bakward slash you need to print two backslashes

        System.out.println("-------------------------------------------------------");

        System.out.println("My favorite TV show is \"Game of Throne\""); // In order to print double quote, we need to

    }


}
